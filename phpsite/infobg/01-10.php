  The first day of the last - the fourth quarter of the Ancient Bulgarian Calendar - is also the day that celebrates the autumn equinox. 
  According to Georgi Velev, the supposed analogous holiday in ancient Bulgarian traditions is 
  <a href="https://en.wikipedia.org/wiki/Beheading_of_John_the_Baptist">Seknovene</a>
  (also known as <i>Seknovenè</i>, <i>Obsichane</i>, <i>Ivan Obsechen</i>, <i>Ivan Koprivan</i>, <i>Tsern Saint John</i>) 
  celebrated by the Orthodox Church as 
  <a href="">Beheading of Saint John the Baptist</a> on the 29<sup>-th</sup> of August.
  Though Seknovene is far from its original (autumn equinox) more than 20 days in time, this holiday still bears the symbolism of the original. 
  According to Volume 3 of the Ethnography of Bulgaria, on that day the day and night do "sekne", that is, they become equal. 
  This symbolism is indicative of the prototype of the holiday - the equinox. 
  In addition, the water "sekne" and becomes dangerous for bathing, while the 
  <a href="https://en.wikipedia.org/wiki/Samodiva_(folklore)">samodivas</a>, snakes and lizards return to their winter grounds. 
  It is interesting that according to popular belief, they begin their activity on 
  Blagovets (under christianity celebrated as 
  <a href="https://en.wikipedia.org/wiki/Annunciation">Annunciation</a>), which day is a supposed analogue of the spring solstice. 
  This confirms once again that Seknovene is in line with the autumn solstice.

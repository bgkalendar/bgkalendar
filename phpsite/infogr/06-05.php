  image on Bulgarian coins.
  <br/><br/>

  According to some folk legends, St. George is the younger twin brother of St. Demetrius, and both are depicted in 
  iconography as horsemen. Saint George always on a white horse facing right, piercing the dragon with his spear, 
  and Saint Demetrius on a red horse facing left (west), piercing the antichrist with his spear.
  <br/><br/>

  Accordingly, it is believed that after St. George's Day the warm days of summer begin, and from St. Dimitrov's Day 
